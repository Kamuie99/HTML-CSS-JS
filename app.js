const calculator = {
    plus: function (a, b){
        console.log(a + b);
    },
    minus: function (a, b){
        console.log(a - b);
    },
    times: function (a, b){
        console.log(a * b);
    },
    divide: function (a, b){
        console.log(a / b);
    },
    power: function (a, b){
        console.log(a ** b);
    },
};

calculator.plus(10, 5);
calculator.minus(10, 5);
calculator.times(10, 5);
calculator.divide(10, 5);
calculator.power(10, 5);
